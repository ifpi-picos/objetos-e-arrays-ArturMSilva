function somaNumeros(num1:number, num2:number) {
    return num1 + num2
}

function subtraiNumeros(num1:number, num2:number) {
    return num1 - num2
}

function multiplicaNumeros(num1:number, num2:number) {
    return num1 * num2
}

function divideNumeros(num1:number, num2:number) {
    return num1 / num2
}

export{
    somaNumeros, divideNumeros, subtraiNumeros, multiplicaNumeros
}